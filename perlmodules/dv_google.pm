method get_access_token () {
###########################################################################
=pod

=head2 get_access_token()

Return a valid OAuth access token using the stored refresh token.

=cut

   my $oauth_client = $client_secret_path;
   my $token_file   = $token_store_path;

   open my $fh1, '<', $oauth_client
       or die "Cannot open $oauth_client: $!";
   my $client = decode_json(do { local $/; <$fh1> });

   open my $fh2, '<', $token_file
      or die "Cannot open $token_file: $!";
   my $tokens = decode_json(do { local $/; <$fh2> });

   my $client_id     = $client->{installed}{client_id}
                    || $client->{web}{client_id};
   my $client_secret = $client->{installed}{client_secret}
                    || $client->{web}{client_secret};

   my $refresh_token = $tokens->{refresh_token}
       or die "No refresh_token in token file";

   my $ua = LWP::UserAgent->new;
   my $res = $ua->post("https://oauth2.googleapis.com/token",
                 { client_id     => $client_id,
                   client_secret => $client_secret,
                   refresh_token => $refresh_token,
                   grant_type    => "refresh_token",
                 });

   die "Token refresh failed: "
       . $res->status_line . "\n"
       . $res->decoded_content
   unless $res->is_success;

   my $data = decode_json($res->decoded_content);
   return $data->{access_token};
}

