method gdrive_put_file ($folder_id, $name, $content) {
###########################################################################
=pod

=head2 gdrive_put_file($name, $content)

Create a file in the configured Google Drive folder.

Arguments:

    $folder_id folder ID
    $name      file name
    $content   file content

Returns the file ID of the created file.

=cut

   my $meta = encode_json({name => $name, parents => [ $folder_id ], });

   my $boundary = "dv_gdrive_" . time;

   my $body = "--$boundary\r\n"
            . "Content-Type: application/json; charset=UTF-8\r\n\r\n"
            . $meta . "\r\n"
            . "--$boundary\r\n"
            . "Content-Type: text/plain\r\n\r\n"
            . $content . "\r\n"
            . "--$boundary--\r\n";

   my $url = "https://www.googleapis.com/upload/drive/v3/files?uploadType=multipart";

   my $res = $self->http_post($url,
                              headers => {"Content-Type" => "multipart/related; boundary=$boundary"},
                              content => $body,
                             );

   die "Drive upload request failed: " . $res->status_line . "\n" . $res->decoded_content
       unless $res->is_success;

   my $data = decode_json($res->decoded_content);
   return $data->{id};
}

