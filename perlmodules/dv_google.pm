=head2 gtask_get_tasks

  my $tasks = $self->gtask_get_tasks($tasklist_id, %opts);

Return an arrayref of tasks in the specified tasklist.

Optional %opts are passed as query parameters (e.g. showCompleted => 'true').

Dies on request failure.

=cut

   my @params;

   while (my ($k, $v) = each %opts) {
      push @params, uri_escape($k) . "=" . uri_escape($v);
   }

   my $qs  = @params ? "?" . join("&", @params) : "";
   my $url = "https://tasks.googleapis.com/tasks/v1/lists/$tasklist_id/tasks$qs";

